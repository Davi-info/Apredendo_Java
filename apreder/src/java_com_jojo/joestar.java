package java_com_jojo;

public class Joestar extends Humano {
    
    private boolean temEstrela;
    private boolean possuiRamon;
    
    public boolean isTemEstrela() {
        return temEstrela;
    }
    public void setTemEstrela(boolean temEstrela) {
        this.temEstrela = temEstrela;
    }
    public boolean isPossuiRamon() {
        return possuiRamon;
    }
    public void setPossuiRamon(boolean possuiRamon) {
        this.possuiRamon = possuiRamon;
    }


    

}
